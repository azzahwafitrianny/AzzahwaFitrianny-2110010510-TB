    private void ViewData(){
        try {
            st = koneksi.createStatement();
            rs = st.executeQuery("SELECT * FROM sewa_pc");
            
            DefaultTableModel tabel = new DefaultTableModel();
            tabel.addColumn("id_pc");
            tabel.addColumn("nama_pc");
            tabel.addColumn("jenis");
            tabel.addColumn("harga");
            
            tabel.getDataVector().removeAllElements();
            tabel.fireTableDataChanged();
            tabel.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                    rs.getString("id_pc"),
                    rs.getString("nama_pc"),
                    rs.getString("jenis"),
                    rs.getString("harga"),
                };
                tabel.addRow(data);
                tabelSewa.setModel(tabel);
        }
        } catch (Exception e){
            
        }
    }

    private void tambahBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBTActionPerformed
        // TODO add your handling code here:
     try {
         Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost/penyewaan_pc", "root", "");
         
         if (idPC.getText().isEmpty() ||
                 namaPC.getText().isEmpty() ||
                 jenisPC.getText().isEmpty() ||
                 hargaPC.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Isi semua data", "Data Simpan", JOptionPane.INFORMATION_MESSAGE);
        return;
       
         }
         String sql = "INSERT INTO sewa_pc(id_pc, nama_pc, jenis, harga) VALUES (?, ?, ?, ?)";
         PreparedStatement ps = koneksi.prepareStatement(sql);
         ps.setString(1, idPC.getText());
         ps.setString(2, namaPC.getText());
         ps.setString(3, jenisPC.getText());
         ps.setString(4, hargaPC.getText());
         
         int rowsInserted = ps.executeUpdate();
        if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(null, "Data Sudah Ditambahkan");
    } else {
        JOptionPane.showMessageDialog(null, "Gagal menambahkan data");
    }
        koneksi.close();
     } catch (Exception e) {
         JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menambahkan data: " + e.getMessage());
    e.printStackTrace();
     }

      
    }//GEN-LAST:event_tambahBTActionPerformed


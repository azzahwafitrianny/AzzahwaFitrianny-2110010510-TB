    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
      try {
         Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost/penyewaan_pc", "root", ""); 
         
          if (transaksiTextField.getText().isEmpty() ||
                  pcTextField.getText().isEmpty() ||
                  namaTextField.getText().isEmpty() ||
                  jenisTextField.getText().isEmpty() ||
                  hargaTextField.getText().isEmpty()){
              JOptionPane.showMessageDialog(null, "Isi semua data", "Data Simpan", JOptionPane.INFORMATION_MESSAGE);
        return;
        
          }
          
          String sql = "INSERT INTO transaksi_pc(id_transaksi, id_pc, nama_pc, jenis, harga) VALUES (?, ?, ?, ?, ?)";
          PreparedStatement ps = koneksi.prepareStatement(sql);
          
          ps.setString(1, transaksiTextField.getText());
          ps.setString(2, pcTextField.getText());
          ps.setString(3, namaTextField.getText());
          ps.setString(4, jenisTextField.getText());
          ps.setString(5, hargaTextField.getText());
          
          int rowsInserted = ps.executeUpdate();
        if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(null, "Data Sudah Ditambahkan");
    } else {
        JOptionPane.showMessageDialog(null, "Gagal menambahkan data");
    }
        koneksi.close();
      }  catch (Exception e) {
         JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menambahkan data: " + e.getMessage());
    e.printStackTrace(); 
      }
    }//GEN-LAST:event_buttonSimpanActionPerformed


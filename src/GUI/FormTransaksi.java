    private void DataView(){
        try {
            st = koneksi.createStatement();
            rs = st.executeQuery("SELECT * FROM transaksi_pc");
            
            DefaultTableModel transaksi = new DefaultTableModel();
            transaksi.addColumn("id_transaksi");
            transaksi.addColumn("id_pc");
            transaksi.addColumn("nama_pc");
            transaksi.addColumn("jenis");
            transaksi.addColumn("harga");
            
            transaksi.getDataVector().removeAllElements();
            transaksi.fireTableDataChanged();
            transaksi.setRowCount(0);
            
            while (rs.next()) {
                Object [] sewa = {
                    rs.getString("id_transaksi"),
                    rs.getString("id_pc"),
                    rs.getString("nama_pc"),
                    rs.getString("jenis"),
                    rs.getString("harga"),
                };
                transaksi.addRow(sewa);
                tabelTransaksi.setModel(transaksi);
            }
                    
        } catch (Exception e){
            
        }
    }

